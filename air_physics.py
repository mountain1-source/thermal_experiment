def get_air_properties(t):
    """
    根据温度 t(°C) 查表获取干空气的物理性质。
    支持线性插值。

    返回7个值：
    ρ:   密度 /kg·m⁻³
    λ:   导热系数 /10⁻²W·m⁻¹·K⁻¹
    ν:   运动粘度 /10⁻⁶m²·s⁻¹
    cp:  比定压热容 /kJ·kg⁻¹·K⁻¹
    α:   热扩散系数 /10⁻⁸m²·s⁻¹
    η:   动力粘度 /10⁻⁶kg·m⁻¹·s⁻¹
    Pr:  普兰德数

    如果 t 超出 -50~1000°C 范围，返回 None
    """
    temps = sorted(AIR_PROPERTIES.keys())
    t_min, t_max = temps[0], temps[-1]

    if t < t_min or t > t_max:
        return None

    # 正好在表中的温度点
    if t in AIR_PROPERTIES:
        return tuple(AIR_PROPERTIES[t])

    # 线性插值：找到相邻的两个温度点
    t_low = max([temp for temp in temps if temp < t])
    t_high = min([temp for temp in temps if temp > t])

    props_low = AIR_PROPERTIES[t_low]
    props_high = AIR_PROPERTIES[t_high]

    # 对每个属性进行线性插值
    ratio = (t - t_low) / (t_high - t_low)
    result = []
    for i in range(7):
        val = props_low[i] + ratio * (props_high[i] - props_low[i])
        result.append(val)

    return tuple(result)


