def get_water_properties(t):
    """
    根据温度 t(°C) 查表获取水的物理性质。
    支持线性插值。

    返回9个值：
    p: 饱和蒸汽压 /kPa
    ρ: 密度 /kg·m⁻³
    H: 焓 /kJ·kg⁻¹
    cp: 比定压热容 /kJ·kg⁻¹·K⁻¹
    λ: 导热系数 /10⁻²W·m⁻¹·K⁻¹
    μ: 粘度 /10⁵Pa·s
    α: 体积膨胀系数 /10⁴K⁻¹
    σ: 表面张力 /10⁻³N·m⁻¹
    Pr: 普兰德数

    如果 t 超出 0~210°C 范围，返回 None
    """
    temps = sorted(WATER_PROPERTIES.keys())
    t_min, t_max = temps[0], temps[-1]

    if t < t_min or t > t_max:
        return None

    # 正好在表中的温度点
    if t in WATER_PROPERTIES:
        return tuple(WATER_PROPERTIES[t])

    # 线性插值：找到相邻的两个温度点
    t_low = max([temp for temp in temps if temp < t])
    t_high = min([temp for temp in temps if temp > t])

    props_low = WATER_PROPERTIES[t_low]
    props_high = WATER_PROPERTIES[t_high]

    # 对每个属性进行线性插值
    ratio = (t - t_low) / (t_high - t_low)
    result = []
    for i in range(9):
        val = props_low[i] + ratio * (props_high[i] - props_low[i])
        result.append(val)

    return tuple(result)


